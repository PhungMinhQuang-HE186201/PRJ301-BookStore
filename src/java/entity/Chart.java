package entity;

/**
 *
 * @author PMQUANG
 */
public class Chart {
    private String label;
    private int value;

    public Chart() {
    }

    public Chart(String label, int value) {
        this.label = label;
        this.value = value;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    @Override
    public String toString() {
        return "Chart{" + "label=" + label + ", value=" + value + '}';
    }
    
}
